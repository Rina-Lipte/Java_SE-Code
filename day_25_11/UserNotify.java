package day_25_11;

public class UserNotify 
{
	private String name;
	private String email;
	
	public UserNotify(String name, String email) {
		super();
		this.name = name;
		this.email = email;
	}
	
	public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

}
